enum axp806_reg {
	AXP806_POWER_STATUS = 0x00,
	AXP806_CHIP_VERSION = 0x03,

	AXP806_OUTPUT_CTRL_1 = 0x10,
	AXP806_OUTPUT_CTRL_2 = 0x11,

	AXP806_DCDCA_VOLTAGE = 0x12,
	AXP806_DCDCB_VOLTAGE = 0x13,
	AXP806_DCDCC_VOLTAGE = 0x14,
	AXP806_DCDCD_VOLTAGE = 0x15,
	AXP806_DCDCE_VOLTAGE = 0x16,

	AXP806_ALDO1_VOLTAGE = 0x17,
	AXP806_ALDO2_VOLTAGE = 0x18,
	AXP806_ALDO3_VOLTAGE = 0x19,

	AXP806_BLDO1_VOLTAGE = 0x20,
	AXP806_BLDO2_VOLTAGE = 0x21,
	AXP806_BLDO3_VOLTAGE = 0x22,
	AXP806_BLDO4_VOLTAGE = 0x23,

	AXP806_CLDO1_VOLTAGE = 0x24,
	AXP806_CLDO2_VOLTAGE = 0x25,
	AXP806_CLDO3_VOLTAGE = 0x26,

	AXP806_IRQ_ENABLE1 = 0x40,
	AXP806_IRQ_ENABLE2 = 0x41,

	AXP806_IRQ_STS1 = 0x48,
	AXP806_IRQ_STS2 = 0x49,
	AXP806_SHUTDOWN = 0x32,
};

#define AXP806_POWER_STATUS_ON_BY_DC	(1 << 0)
#define AXP806_POWER_STATUS_VBUS_USABLE	(1 << 4)

#define AXP806_OUTPUT_CTRL_EXTEN	(1 << 0)
#define AXP806_OUTPUT_CTRL_ALDO2	(1 << 6)

#define AXP806_IRQ5_PEK_UP		(1 << 6)
#define AXP806_IRQ5_PEK_DOWN		(1 << 5)

#define AXP806_POWEROFF			(1 << 7)

/* For axp_gpio.c */
#define AXP_POWER_STATUS		0x00
#define AXP_POWER_STATUS_VBUS_PRESENT		(1 << 5)
#define AXP_GPIO0_CTRL			0x90
#define AXP_GPIO1_CTRL			0x92
#define AXP_GPIO2_CTRL			0x93
#define AXP_GPIO_CTRL_OUTPUT_LOW		0x00 /* Drive pin low */
#define AXP_GPIO_CTRL_OUTPUT_HIGH		0x01 /* Drive pin high */
#define AXP_GPIO_CTRL_INPUT			0x02 /* Input */
#define AXP_GPIO_STATE			0x94
#define AXP_GPIO_STATE_OFFSET			4
